#Fatiando listas
#lista = [start:stop:step]
#start = indice inicial, default = 0
#stop = indice final, default = numElementosLista
#step = incremento de indice, default = 1

lista = "Bem vindo ao curso de python"

print(lista[10])
print(lista[:20])
print(lista[10:20])
print(lista[::2])
print(lista[::3])
print(lista[::-1])
